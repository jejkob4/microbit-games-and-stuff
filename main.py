#V této hře se micro:bit náhodně rozsvítí LED a hráč musí co nejrychleji stisknout tlačítko, aby ji vypnul. 
#Micro:bit poté zobrazí reakční čas hráče. 
#Hra by mohla mít několik úrovní, kde se LED rozsvítí na kratší dobu nebo kde bude hráč mít méně času na reakci, jak se bude postupovat do dalších úrovní.
#Pro implementaci této hry by bylo potřeba připojit tlačítko a LED k micro:bitu a naprogramovat micro:bit tak, 
#aby náhodně rozsvítil LED a detekoval stisk tlačítka k jejímu vypnutí. Také by bylo možné přidat zvukové efekty nebo animace LED, aby byla hra pro hráče zajímavější.
